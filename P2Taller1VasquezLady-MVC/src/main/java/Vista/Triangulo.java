    private static boolean esRectangulo(double a, double b, double c) {
        // Ordenar los lados
        double[] lados = {a, b, c};
        java.util.Arrays.sort(lados);
        // Aplicar el teorema de Pitágoras
        return (lados[0] * lados[0] + lados[1] * lados[1] == lados[2] * lados[2]);
    }
    
    
