        // TODO add your handling code here:
        boolean datosValidos = true;
        boolean datosValidosLado=true;// Variable para verificar la validez de los datos
        // CONTROL DE LA BASE
try {
    base = Double.parseDouble(txtNumA.getText());
    if (base < 1) {
        jlErrorA.setForeground(Color.RED);
        jlErrorA.setText("Error: Rango incorrecto.");
        datosValidos = false; // Marcar como no válido
    } else {
        jlErrorA.setForeground(Color.GREEN);
        jlErrorA.setText("Número válido: " + base);
    }
} catch (NumberFormatException e) {
    jlErrorA.setForeground(Color.RED);
    jlErrorA.setText("Error: Debe ingresar un número válido.");
    datosValidos = false; // Marcar como no válido
}

